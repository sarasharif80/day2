		//DaytaType variableName=variableValue;
		
		int luckyNumber=7;
		int oddNumber=11;
		System.out.println(luckyNumber);
		System.out.println(oddNumber);
		
		//Byte example
		byte count=44;
		System.out.println(count);
		
		//short Example
		short zipcode=1234;
		System.out.println(zipcode);
		
		/*int Example*/
		int size=444;
		System.out.println(size);
		
		/** long example*/
		long pupulation=123456789L;
		System.out.println(pupulation);
		
		//float Example
		float inches=64.345f;
		System.out.println(inches);
		
		//double example
		double price= 5.99;
		System.out.println(price);
		
		/**boolean example*/

		boolean isJavaFun=true;
		boolean amITired=false;
		System.out.println(isJavaFun);
		System.out.println(amITired);
		
		// character -->char example
		char mygrade= 'A';
		char gender= 'F';
		System.out.println(mygrade);
		System.out.println(gender);
		
